public class ListaTerceirizacao {
    private String npj;
    private String origem;
    private String destino;
    private String controle;
    private String dataAtendimento;    
    private String tipo;
    private String itensdoSubsidio;
    private String estado;

    public String getControle() {
        return controle;
    }

    public void setControle(String controle) {
        this.controle = controle;
    }

    public String getDataAtendimento() {
        return dataAtendimento;
    }

    public void setDataAtendimento(String dataAtendimento) {
        this.dataAtendimento = dataAtendimento;
    }

    public String getDestino() {
        return destino;
    }

    public void setDestino(String destino) {
        this.destino = destino;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getItensdoSubsidio() {
        return itensdoSubsidio;
    }

    public void setItensdoSubsidio(String itensdoSubsidio) {
        this.itensdoSubsidio = itensdoSubsidio;
    }

    public String getNpj() {
        return npj;
    }

    public void setNpj(String npj) {
        this.npj = npj;
    }

    public String getOrigem() {
        return origem;
    }

    public void setOrigem(String origem) {
        this.origem = origem;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
    
    
}
